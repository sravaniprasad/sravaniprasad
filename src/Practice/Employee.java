package Practice;
public  class Employee{
		private String empName;

		private int emplID;

		private int empAge;

		public String getName() {
			return empName;
		}

		public void setName(String name) {
			empName = name;
		}

		public int getEmpID() {
			return emplID;
		}

		public void setEmpID(int empID) {
			emplID = empID;
		}

		public int getAge() {
			return empAge;
		}

		public void setAge(int age) {
			empAge = age;
		}

		
		
		}


